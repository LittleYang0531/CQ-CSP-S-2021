#include<bits/stdc++.h>
using namespace std;
const int mod=1e9+7;
int n,k,ans;
string s;
inline void dfs(int now,int x,int y,int m){
	if(y>x||m>k)
		return;
	if(y+n-now<x)
		return;
	if(now==n){
		ans++;
		if(ans>mod)
			ans-=mod;
		return;
	}
	if(s[now]=='(')
		dfs(now+1,x+1,y,0);
	else if(s[now]=='*')
		dfs(now+1,x,y,m+1);
	else if(s[now]==')')
		dfs(now+1,x,y+1,0);
	else{
		dfs(now+1,x+1,y,0);
		if(x!=y)
			dfs(now+1,x,y,m+1);
		dfs(now+1,x,y+1,0);
	}
}
